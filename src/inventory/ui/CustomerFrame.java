    private void customerTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_customerTableMouseClicked
        try {
            // TODO add your handling code here:
            int row = customerTable.getSelectedRow();
            String customerId = customerTable.getModel().getValueAt(row, 0).toString();

            Customer customer = CustomerController.getCustomerById(customerId);
            nameTxt.setText(customer.getName());
            nicTxt.setText(customer.getNic());
            nicTxt.setEditable(false);
            genderCombo.setSelectedItem(customer.getGender());
            addressTxt.setText(customer.getAddress());
            dobTxt.setText(customer.getDob());
            telTxt.setText(customer.getTel());
            emailTxt.setText(customer.getEmail());
            loyaltyCardTxt.setText(customer.getLoyaltyCard());
            CustomerIdTxt.setText(customer.getNic());
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(CustomerFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_customerTableMouseClicked

    private void DeleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteBtnActionPerformed
        // TODO add your handling code here:
        int row = customerTable.getSelectedRow();
        String customerId = customerTable.getModel().getValueAt(row, 0).toString();
        int p = JOptionPane.showConfirmDialog(rootPane, "Do you really want to Delete? ", "Delete", JOptionPane.YES_NO_OPTION);
        if (p == 0) {
            try {
                CustomerController.deleteCustomer(customerId);
                updateCustomerTable();
                clearTextFields();
            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(CustomerFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_DeleteBtnActionPerformed

