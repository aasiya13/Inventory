    private void SearchNicTxtKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_SearchNicTxtKeyReleased
        // TODO add your handling code here:
        try {
            ResultSet resultSet = CustomerController.getInfoCustomerTableById(SearchNicTxt.getText());
            customerTable.setModel(DbUtils.resultSetToTableModel(resultSet));

            TableColumn col = customerTable.getColumnModel().getColumn(0);
            col.setHeaderValue("National ID");
            col.setPreferredWidth(100);
            TableColumn col2 = customerTable.getColumnModel().getColumn(1);
            col2.setHeaderValue("Customer Name");
            col2.setPreferredWidth(100);
            TableColumn col3 = customerTable.getColumnModel().getColumn(2);
            col3.setHeaderValue("Telephone");
            col3.setPreferredWidth(100);
        } catch (ClassNotFoundException | SQLException e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }//GEN-LAST:event_SearchNicTxtKeyReleased

